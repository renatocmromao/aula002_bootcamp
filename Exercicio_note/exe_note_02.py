num1 = int(input('Digite um número: '))
num2 = int(input('Digite outro número: '))

soma = (num1 + num2)
subtracao = (num1 - num2)
multiplicacao = (num1 * num2)
divisao = (num1 / num2)

print(f'A Soma dos números será {soma}.\nA Subtração dos números será {subtracao}.')
print(f'A Multiplicação dos números será {multiplicacao}.\nA Divisão dos números será {divisao}.')